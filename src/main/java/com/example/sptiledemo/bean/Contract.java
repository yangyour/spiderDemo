package com.example.sptiledemo.bean;

public class Contract {
    private String code;
    //公司名称
    private String name;
    //合同名称
    private String conName;
    //项目状态
    private String status;
    //终止时间
    private String time;
    //合同类型编码
    private String type;
    //印务总金额
    private String printing;
    //底稿总金额
    private String manuscript;
    //可研合同金额
    private String feasibility;
    //信批合同金额
    private String letterApproved;
    //财务数据核对
    private String finance;
    //ELS
    private String els;
    //YXZ
    private String yxz;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getConName() {
        return conName;
    }

    public void setConName(String conName) {
        this.conName = conName;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPrinting() {
        return printing;
    }

    public void setPrinting(String printing) {
        this.printing = printing;
    }

    public String getManuscript() {
        return manuscript;
    }

    public void setManuscript(String manuscript) {
        this.manuscript = manuscript;
    }

    public String getFeasibility() {
        return feasibility;
    }

    public void setFeasibility(String feasibility) {
        this.feasibility = feasibility;
    }

    public String getLetterApproved() {
        return letterApproved;
    }

    public void setLetterApproved(String letterApproved) {
        this.letterApproved = letterApproved;
    }

    public String getFinance() {
        return finance;
    }

    public void setFinance(String finance) {
        this.finance = finance;
    }

    public String getEls() {
        return els;
    }

    public void setEls(String els) {
        this.els = els;
    }

    public String getYxz() {
        return yxz;
    }

    public void setYxz(String yxz) {
        this.yxz = yxz;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
